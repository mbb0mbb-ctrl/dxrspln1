// Ay için toplam konu sayısı
export const getMonthTopicCount = (monthId) => {
  const month = comprehensiveData.months.find(m => m.id === monthId);
  if (!month) return 0;

  let count = 0;
  Object.values(month.subjects).forEach(subjectTopics => {
    count += subjectTopics.length;
  });

  return count;
};
