// Haftalık planlar için yardımcı fonksiyonlar
export const getMonthlyTopicsPool = (monthId) => {
  const month = comprehensiveData.months.find(m => m.id === monthId);
  if (!month) return {};

  // Aylık plandaki tüm konuları ders bazında döndür
  const topicsPool = {};
  Object.entries(month.subjects).forEach(([subject, topics]) => {
    if (topics.length > 0) {
      topicsPool[subject] = topics.map(topic => ({
        id: `${monthId}-${subject}-${topic}`,
        text: topic,
        subject: subject,
        monthId: monthId,
        assignedWeeks: [] // Hangi haftalara atandığını takip et
      }));
    }
  });

  return topicsPool;
};

